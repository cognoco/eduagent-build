# Wrapper around seed-and-run.sh for dictation review flows (DICT-07..10).
#
# The dictation review screen is reached after the LLM analyses a photo of
# handwritten text captured by the camera. To avoid requiring a real camera
# session in E2E, this wrapper:
#   1. Generates a minimal test JPEG (white background with a short sentence)
#      and pushes it to the emulator's gallery via ADB.
#   2. Triggers a MediaScanner broadcast so the image appears in the gallery
#      picker immediately.
#
# The mobile dev-client is built with EXPO_PUBLIC_E2E=1 (set in .env.local),
# which makes complete.tsx expose the `complete-pick-gallery` testID button.
# The Maestro flow taps that button instead of complete-check-writing, then
# selects the pre-planted image from the gallery picker.
#
# Usage:
#   ./e2e/scripts/seed-and-run-dictation-review.sh <scenario> <flow-file> [maestro-args...]
#
# Scenarios:
#   dictation-with-mistakes   — flow plants a JPEG with deliberate errors
#   dictation-perfect-score   — flow plants a JPEG with correct text
#
# IMAGE_TEXT env var overrides the sentence written into the test JPEG.
# Defaults to text with a deliberate misspelling so the LLM finds mistakes.
#
# Prerequisites:
#   - Python3 available (generates the test JPEG via a minimal script)
#   - ADB connected to emulator
#   - Dev-client built with EXPO_PUBLIC_E2E=1 in apps/mobile/.env.local
#   - API server + Metro running

